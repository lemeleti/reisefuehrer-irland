		in vielen Pubs in Dublin darf Live Musik nicht fehlen, diese spielt
		vorne beim ehemaligen Altar. The Church ist das beste Pub, um ein
		Guinness zu trinken, weil hier der Gr&uuml;nder von Guinness einst
		heiratete.</p>
