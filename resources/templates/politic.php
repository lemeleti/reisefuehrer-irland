<hr class="my-4">
<div class="content">
    <p>Das Geb&auml;ude ist 1802 im neoklassizistischen Stil von James
        Gandon erbaut worden. Seit dieser Zeit wird es als
        Gerichtsgeb&auml;ude ben&uuml;tzt. Four Courts heisst es, weil die
        vier Gerichtsh&ouml;fe darin untergebracht sind. Es sind dies der
        Oberste Gerichtshof (Supreme Court), das Oberste Zivilgericht (High
        Court), das Berufungsgericht der Stadt Dublin (Dublin Circuit) und das
        bis 2010 dort beheimatete Strafgericht (Central Criminal Court). Im
        Osteraufstand wurde der Four Courts von der Irish Republican
        Brotherhood belagert und von den britischen Truppen beschossen. Dabei
        wurde bei einem Angriff das Nationale Archiv getroffen, weshalb
        Dokumente aus fast 1000 Jahren verloren gingen. Darunter sind auch die
        Pl&auml;ne des Architekten des Four Courts. Nach der Restauration
        wurde das Geb&auml;ude 1932 wieder ge&ouml;ffnet und die
        Gerichtsh&ouml;fe zogen erneut ein. Es gibt &ouml;ffentliche
        Gerichtsverhandlungen, bei denen der Besuch der Innenr&auml;ume oder
        mindestens der beindruckenden Eingangshalle m&ouml;glich ist.</p>
