<div class="content">
	<p class="mb-4">Die Tour startet beim Gerichtsgeb&auml;ude von Dublin. Dass die vier wichtigen Gerichte von Irland beherbergt. Das Geb&auml;ude spielt wie viele weitere eine wichtige Rolle beim Osteraufstand in Dublin. Das n&auml;chste Geb&auml;ude wurde urspr&uuml;nglich als B&ouml;rse genutzt, bis es der Stadt Dublin geh&ouml;rt und von der Regierung genutzt wird. Heute ist es nicht mehr im Gebrauch als Rathaus aber ist sehr sehenswert und wird einmal in Monat noch wie dazumal von der Regierung als Sitzungsort verwendet. Das Irish Parliament ist ein monumentales Geb&auml;ude das heute seiner ehemaligen Nutzung nicht mehr nachkommt. Heute ist die Bank of Ireland im Geb&auml;ude zu hause. Der ehemalige Herzogspalast ist unsere n&auml;chste Station, heute Leinster House genannt. Seit einiger Zeit sind dort die zwei Kammern beheimatet. Als letzte Station folgen die Gouvernment Buildings die heute viele der wichtigen Sitze beherbergt. Wie das Ministerium des Premierministers. Mit dem aktuellen Premierminister Leo Varadkar.</p>
