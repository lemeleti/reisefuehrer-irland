        wurde leider bei einem Angriff das Nationale Archiv getroffen deshalb
        Dokumente aus fast 1000 Jahren verloren. Darunter sind auch die
        Pl&auml;ne des Architekten des Four Courts. Das Geb&auml;ude wurde 1932
        nach der Restauration wieder ge&ouml;ffnet und die Gerichtsh&ouml;fe
        sind wieder eingezogen. Es gibt &ouml;ffentliche Gerichtsverhandlungen
